export function seedGifts(opts: { replace?: boolean } = {}) {
  const existing = knowledgeBase.listGifts(false);
  if (existing.length && !opts.replace) {
    console.log(`Catalog already has ${existing.length} gifts — skip seed (use --force to replace)`);
    return;
  }
  if (existing.length && opts.replace) {
    getDb().prepare("DELETE FROM gifts").run();
    console.log(`Removed ${existing.length} existing gifts`);
  }
  for (const g of SEED_GIFTS) {
    knowledgeBase.createGift(g);
  }
  console.log(`Seeded ${SEED_GIFTS.length} gifts`);
}

if (import.meta.url === `file://${process.argv[1]}`) {
  const replace = process.argv.includes("--force") || process.argv.includes("--replace");
  seedGifts({ replace });
}
