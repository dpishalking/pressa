export function seedGifts() {
  const existing = knowledgeBase.listGifts(false);
  if (existing.length) {
    console.log(`Catalog already has ${existing.length} gifts — skip seed`);
    return;
  }
  for (const g of SEED_GIFTS) {
    knowledgeBase.createGift(g);
  }
  console.log(`Seeded ${SEED_GIFTS.length} gifts`);
}

if (import.meta.url === `file://${process.argv[1]}`) {
  seedGifts();
}
